    <li>When disassembling furniture, keep all screws and other hardware in a bag</li>
    <li>Put a label on the bag and secure it to the piece of furniture. Duvets, blankets, coverings, and other items that take up a lot of room can be stored in vacuumable bags. It will be easier to put them in a box or container once you have removed all of the air</li>
    <li>Makeup, especially eyeshadow palettes, is extremely sensitive. Place little cotton pads within pallets to keep the contents from breaking if you want to avoid breakage</li>
